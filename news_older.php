hi, i'm back.  non-pro version is fixed, you have to install new code if you registered before today.  registration will be open sporadically.  <br /><br />
attack of the clones:  i've noticed a lot of spyspace ripoff sites popping up in my absence, some even using the name "spyspace" or directly stealing the layout of this site along with the idea.  some of these geniuses even write to me asking for help on getting their code to work.  fantastic! i would not sign up for these, because every one i've seen steals your myspace cookie to operate!  what does that mean?  it means whoever is running the site can easily log into myspace as you and read your messages, or do whatever.  spyspace doesn't touch your myspace cookie, and i couldn't give less of a shit about hacking anyone's profile.  you can watch the connections with a tool like livehttpheaders or iewatch (google it, poindexter) and see that no cookie data is sent to the spyspace database.  also, the clones have no idea what they are doing:  the quicker the concept spreads, the sooner it stops working.  in fact, i'm pretty sure there's no way it will last longer than two more weeks.  thanks a lot, guys.
